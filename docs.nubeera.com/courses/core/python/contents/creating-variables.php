		<h3> Creating Variables </h3>
		Python has no command for declaring a variable.<br>
		A variable is created the moment you first assign a value to it.<br>
		Variables do not need to be declared with any particular type, and can even change type after they have been set.<br>
		If you want to specify the data type of a variable, this can be done with casting.<br>
		You can get the data type of a variable with the type() function.<br>
		String variables can be declared either by using single or double quotes<br>
		Variable names are case-sensitive.<br>

		<h3>Variable Names</h3>
		A variable can have a short name (like x and y) or a more descriptive name (age, carname, total_volume). Rules for Python variables:<br>
		A variable name must start with a letter or the underscore character<br>
		A variable name cannot start with a number<br>
		A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )<br>
		Variable names are case-sensitive (age, Age and AGE are three different variables)<br>

		<h3>Multi Words Variable Names</h3>
		Variable names with more than one word can be difficult to read.<br>
		There are several techniques you can use to make them more readable:<br>

		<h3>Camel Case</h3>
		Each word, except the first, starts with a capital letter<br>

		<h3>Pascal Case</h3>
		Each word starts with a capital letter<br>

		<h3>Snake Case</h3>
		Each word is separated by an underscore character<br>

		<h3>Many Values to Multiple Variables</h3>
		Python allows you to assign values to multiple variables in one line:<br>

		<h3>One Value to Multiple Variables</h3>
		And you can assign the same value to multiple variables in one line<br>

		<h3>Unpack a Collection</h3>
		If you have a collection of values in a list, tuple etc. Python allows you extract the values into variables. This is called <i>unpacking</i>.<br>

		<h3>Output Variables</h3>
		The Python print statement is often used to output variables.<br>
		To combine both text and a variable, Python uses the + character<br>
		You can also use the + character to add a variable to another variable<br>
		For numbers, the + character works as a mathematical operator<br>
		If you try to combine a string and a number, Python will give you an error<br>

		<h3>Global Variables</h3>
		Variables that are created outside of a function (as in all of the examples above) are known as global variables.<br>
		Global variables can be used by everyone, both inside of functions and outside.<br>
		If you create a variable with the same name inside a function, this variable will be local, and can only be used inside the function. The global variable with the same name will remain as it was, global and with the original value<br>

		<h3>The global Keyword</h3>
		Normally, when you create a variable inside a function, that variable is local, and can only be used inside that function.<br>
		To create a global variable inside a function, you can use the global keyword.<br>
		Use the global keyword if you want to change a global variable inside a function.<br>

