                    <br>A simple text editor is all you need to learn HTML.
                    <h3 id="Learn HTML Using Notepad or TextEdit" class="heading-anchor">Learn HTML Using Notepad or TextEdit</h3>
                    Web pages can be created and modified by using professional HTML editors.<br>

                    However, for learning HTML we recommend a simple text editor like Notepad (PC) or TextEdit (Mac).<br>

                    We believe in that using a simple text editor is a good way to learn HTML.<br>

                    Follow the steps below to create your first web page with Notepad or TextEdit.<br>

                    <h3 id="Step 1: Open Notepad (PC)" class="heading-anchor">Step 1: Open Notepad (PC)</h3>
                    Windows 8 or later:<br>

                    Open the Start Screen (the window symbol at the bottom left on your screen). Type Notepad.<br>

                    Windows 7 or earlier:<br>

                    Open Start > Programs > Accessories > Notepad<br>


                    <h3 id="Step 1: Open TextEdit (Mac)" class="heading-anchor">Step 1: Open TextEdit (Mac)</h3>
                    Open Finder > Applications > TextEdit<br>

                    Also change some preferences to get the application to save files correctly. In Preferences > Format > choose "Plain Text"<br>

                    Then under "Open and Save", check the box that says "Display HTML files as HTML code instead of formatted text".<br>

                    Then open a new document to place the code.<br>

                    <h3 id="Step 2: Write Some HTML" class="heading-anchor">Step 2: Write Some HTML</h3>
                    Write or copy the following HTML code into Notepad:
                    <div class="w3-example" style="border:2px;background-color:#D6EEEE;border-radius: 25px;padding: 10px;">
                        <h3>Example</h3>
                        <div class="w3-code notranslate htmlHigh" style="border:2px;background-color:#e6e6e6;padding: 10px;">
                            &lt;!DOCTYPE html&gt;<br>&lt;html&gt;<br>&lt;body&gt;<br><br>&lt;h1&gt;My First Heading&lt;/h1&gt;<br><br>
                            &lt;p&gt;My first paragraph.&lt;/p&gt;<br><br>&lt;/body&gt;<br>&lt;/html&gt;
                        </div>
                    </div><br><br>
                    <img src="https://www.w3schools.com/html/img_notepad.png" alt="Notepad" width="auto">

                    <h3 id="Step 3: Save the HTML Page" class="heading-anchor">Step 3: Save the HTML Page</h3>
                    Save the file on your computer. Select File > Save as in the Notepad menu.<br>

                    Name the file "index.htm" and set the encoding to UTF-8 (which is the preferred encoding for HTML files).<br>
                    <img src="https://www.w3schools.com/html/img_saveas.png" alt="save">
                    <br><br>
                    <div class="notes">
                        <strong>Tip</strong>: You can use either .htm or .html as file extension. There is no difference, it is up to you.
                    </div>

                    <h3 id="Step 4: View the HTML Page in Your Browser" class="heading-anchor">Step 4: View the HTML Page in Your Browser</h3>
                    Open the saved HTML file in your favorite browser (double click on the file, or right-click - and choose "Open with").<br>

                    The result will look much like this:<br>
                    <img src="https://www.w3schools.com/html/img_chrome.png" alt="img">

