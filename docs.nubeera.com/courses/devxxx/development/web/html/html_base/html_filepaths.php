                    <h3 id="HTML File Paths" class="heading-anchor">HTML File Paths</h3>
                    A file path describes the location of a file in a web site's folder structure.<br>
                    File paths are used when linking to external files, like:<br>
                    <ul>
                        <li>Web pages</li>
                        <li>Images</li>
                        <li>Style sheets</li>
                        <li>JavaScripts</li>
                    </ul>
                    <hr>


                    <h3 id="Absolute File Paths" class="heading-anchor">Absolute File Paths</h3>
                    An absolute file path is the full URL to a file:<br>
                    <div class="w3-example" style="border:2px;background-color:#D6EEEE;border-radius: 25px;padding: 10px;">
                        <h3>Example</h3>
                        <div class="w3-code notranslate htmlHigh" style="border:2px;background-color:#e6e6e6;padding: 10px;">
                            &lt;img src=&quot;https://www.w3schools.com/images/picture.jpg&quot;
                            alt=&quot;Mountain&quot;&gt;
                        </div>
                    </div>
                    <hr>

                    <h3 id="Relative File Paths" class="heading-anchor">Relative File Paths</h3>
                    A relative file path points to a file relative to the current page.<br>

                    In the following example, the file path points to a file in the images folder located at the root of the current web:<br>
                    <div class="w3-example" style="border:2px;background-color:#D6EEEE;border-radius: 25px;padding: 10px;">
                        <h3>Example</h3>
                        <div class="w3-code notranslate htmlHigh" style="border:2px;background-color:#e6e6e6;padding: 10px;">
                            &lt;img src=&quot;/images/picture.jpg&quot;
                            alt=&quot;Mountain&quot;&gt;
                        </div>
                    </div>
                    <hr>

                    <h3>Best Practice</h3>
                    It is best practice to use relative file paths (if possible).<br>

                    When using relative file paths, your web pages will not be bound to your current base URL. All links will work on your own computer (localhost) as well as on your current public domain and your future public domains.
